from project import bin, hex, oct, close

def main():
    test_bin()
    test_hex()
    test_oct()


def test_bin():
    assert bin(1) == "0001"
    assert bin(10) == " 1010"
    assert bin(1000) == "0011 1110 1000"
    assert bin(104800) == "0001 1001 1001 0110 0000"
    assert bin(9223372036854775807) == "0111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111"
    assert bin(-1) == "1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 "
    assert bin(-1000) == "1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1100 0001 1000 "
    assert bin(-500000) == "1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1000 0101 1110 1110 0000 "


def test_hex():
    assert hex("0001") == "1"
    assert hex("1010") == "A"
    assert hex("0011 1110 1000") == "3E8"
    assert hex("0001 1001 1001 0110 0000") == "1 9960"
    assert hex("0111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111") == " 7FFF FFFF FFFF FFFF"
    assert hex("1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111") == " FFFF FFFF FFFF FFFF"
    assert hex("1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1100 0001 1000") == " FFFF FFFF FFFF FC18"
    assert hex("1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1000 0101 1110 1110 0000") == " FFFF FFFF FFF8 5EE0"


def test_oct():
    assert oct("0001", 1) == "1"
    assert oct("1010", 10) == "12"
    assert oct("0011 1110 1000", 1000) == "1 750"
    assert oct("0001 1001 1001 0110 0000", 104800) == " 314 540"
    assert oct("0111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111", 9223372036854775807) == " 777 777 777 777 777 777 777"
    assert oct("1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111", -1) == "1 777 777 777 777 777 777 777"
    assert oct("1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1100 0001 1000", -1000) == "1 777 777 777 777 777 776 030"
    assert oct("1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1111 1000 0101 1110 1110 0000", -500000) == "1 777 777 777 777 776 057 340"

if __name__ == "__main__":
    main()
